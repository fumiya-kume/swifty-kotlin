fun charPlusInt() {
    val c: Char = 'a'
    val next = c + 1
    println(next)
}

fun charMinusInt() {
    val c: Char = 'z'
    val prev = c - 1
    println(prev)
}

fun charMinusChar() {
    val diff = 'z' - 'a'
    println(diff)
}

fun charComparison() {
    val lt = 'a' < 'z'
    val eq = 'a' == 'a'
    println(lt)
    println(eq)
}
