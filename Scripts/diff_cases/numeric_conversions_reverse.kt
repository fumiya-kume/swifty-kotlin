fun main() {
    val d = 3.14
    println(d.toInt())
    println(d.toLong())
    println(d.toFloat())

    val f = 2.5f
    println(f.toInt())
    println(f.toLong())
    println(f.toDouble())

    val l = 42L
    println(l.toFloat())
    println(l.toDouble())
    println(l.toInt())
    println(l.toByte())
    println(l.toShort())

    val i = 100
    println(i.toDouble())
}
